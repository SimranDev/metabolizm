const { withAppBuildGradle } = require('expo/config-plugins');

/**
 * Align androidx.work versions.
 *
 * @react-native-google-signin/google-signin brings androidx.work:work-runtime
 * 2.8.1, while react-native-android-widget (used by the home-screen widgets)
 * brings work-runtime-ktx:2.7.1. The Kotlin extension classes
 * (OneTimeWorkRequestKt, PeriodicWorkRequestKt, …) were folded from the -ktx
 * artifact into the main artifact at 2.8, so the two versions collide at
 * :app:checkDebugDuplicateClasses and the build fails.
 *
 * Pinning both to 2.8.1 makes work-runtime-ktx a thin stub over work-runtime,
 * removing the duplicate. `android/` is regenerated by prebuild, so this has to
 * be a config plugin rather than an edit to the generated build.gradle.
 */
const MARKER = '// workmanager-version-align';

const SNIPPET = `
${MARKER}
configurations.all {
    resolutionStrategy {
        force "androidx.work:work-runtime:2.8.1"
        force "androidx.work:work-runtime-ktx:2.8.1"
    }
}
`;

module.exports = function withWorkManagerFix(config) {
  return withAppBuildGradle(config, (cfg) => {
    if (!cfg.modResults.contents.includes(MARKER)) {
      cfg.modResults.contents += SNIPPET;
    }
    return cfg;
  });
};
